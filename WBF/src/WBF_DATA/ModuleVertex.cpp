void CModuleVertex::SetDefaultData()
{
	if (!Empty()) return;

	auto SZ_POS = sizeof(float) * 3;
	auto SZ_NORMAL = sizeof(float) * 3;
	auto SZ_TEXCORD = sizeof(float) * 2;
	auto SZ_DATA = (SZ_POS + SZ_NORMAL + SZ_TEXCORD);

	auto szNum = sizeof(g_vertices) / SZ_DATA;
	for (auto indx = 0; indx < static_cast<int>(szNum); ++indx)
	{
		CEntityVertex Data;
		Data.dbKey = GetNewKey();
		Data.vPos = glm::vec3(g_vertices[indx * 8 + 0], g_vertices[indx * 8 + 1], g_vertices[indx * 8 + 2]);
		Data.vNormal = glm::vec3(g_vertices[indx * 8 + 3], g_vertices[indx * 8 + 4], g_vertices[indx * 8 + 5]);
		Data.vTexcord = glm::vec2(g_vertices[indx * 8 + 6], g_vertices[indx * 8 + 7]);

		InsertNU(Data);
	}
}
